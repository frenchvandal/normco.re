export type OcticonName = keyof typeof OCTICON_PATHS;

export function resolveOcticonUrl(
  name: string,
  variant = "16",
  resolveIcon?: IconResolver,
): string {
  return resolveIcon?.(name, "octicons", variant) ??
    `${OCTICONS_CDN_BASE}/${name}-${variant}.svg`;
}

export function resolveSimpleIconUrl(
  name: string,
  resolveIcon?: IconResolver,
): string {
  return resolveIcon?.(name, "simpleicons") ??
    `${SIMPLE_ICONS_CDN_BASE}/${name}.svg`;
}

export function getOcticonPathData(
  name: OcticonName,
): readonly string[] {
  return OCTICON_PATHS[name];
}

export function renderOcticonMarkup(
  name: OcticonName,
  className: string,
  {
    width = 16,
    height = 16,
  }: Readonly<{
    width?: number;
    height?: number;
  }> = {},
): string {
  const paths = getOcticonPathData(name).map((path) =>
    `<path d="${escapeHtml(path)}"></path>`
  ).join("");

  return `<svg class="${
    escapeHtml(className)
  }" width="${width}" height="${height}" viewBox="0 0 16 16" fill="currentColor" aria-hidden="true" focusable="false" data-icon="${
    escapeHtml(name)
  }">${paths}</svg>`;
}
